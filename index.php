            <h3>O que é a sala de aula invertida?</h3> 
            <br/>
            <p>
    Antes de tudo, é importante conhecer esse conceito. Também conhecido como <em>flipped classroom</em>, o método consiste em fazer o aluno estudar antes da aula e tirar suas dúvidas durante o momento de sala. É considerado um método eficiente visto que o horário de aula é mais dinâmico e melhor aproveitado.</p>
    <p>Os professores têm a função de acompanhar o aprendizado do aluno como um tutor, fornecendo material de pesquisa e tirando dúvidas, mas não necessariamente dando aula. Um problema que poucos educadores prestam  atenção nesse sistema é que justamente pelo aluno ser o protagonista, especialmente em um cenário tão atípico, o aprendizado pode tornar-se chato e desinteressante, principalmente durante o ensino fundamental.
    
    
            </p>   
