            <h3>Como o Maranhão se comportou no início da pandemia?</h3>
            <br/>
            <p> O Maranhão entrou em estado de alerta em meados de março de 2020. Nesse período as aulas foram paralisadas e depois de um tempo foi declarado em algumas escolas férias antecipadas de meio de ano, a fim de não criar um crescente atraso no ensino, assim como dar tempo para escolas e universidades não preparadas migrarem para o sistema remoto. Algumas instituições já preparadas migraram rapidamente para o sistema remoto. Muitas escolas particulares adaptaram-se.
            </p>
    	<p>O perigo está nas escolas públicas. Demorou demasiadamente para o governo propor medidas e aplicar o ensino remoto em suas instituições. O IFMA (Instituto Federal de Ensino e Tecnologia do Maranhão) campus Monte Castelo, por exemplo, teve as aulas do terceiro ano iniciadas apenas em setembro. Algumas instituições sequer disponibilizaram material para os educadores e alunos terem acesso ao ensino remoto, e outras nem mesmo ensino disponibilizaram.
    	</p>
    	<p>Uma metodologia bastante discutida e proposta para o ensino remoto foi a adesão do sistema de sala de aula invertida. Muitas instituições usam tal método.
